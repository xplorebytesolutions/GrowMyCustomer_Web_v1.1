const formatBytes = bytes => {
  const n = Number(bytes);
  if (!Number.isFinite(n) || n <= 0) return null;
  const units = ["B", "KB", "MB", "GB"];
  let value = n;
  let i = 0;
  while (value >= 1024 && i < units.length - 1) {
    value /= 1024;
    i += 1;
  }
  const fixed = value >= 100 ? 0 : value >= 10 ? 1 : 2;
  return `${value.toFixed(fixed)} ${units[i]}`;
};

const resolveChatMediaType = msg => {
  const explicit = String(msg?.mediaType ?? msg?.MediaType ?? "")
    .trim()
    .toLowerCase();

  const mime = String(msg?.mimeType ?? msg?.MimeType ?? "")
    .split(";")[0]
    .trim()
    .toLowerCase();

  const fileName = String(msg?.fileName ?? msg?.FileName ?? "")
    .trim()
    .toLowerCase();

  if (explicit === "location") return "location";

  if (mime) {
    if (mime === "application/pdf") return "document";
    if (mime.startsWith("image/")) return "image";
    if (mime.startsWith("video/")) return "video";
    if (mime.startsWith("audio/")) return "audio";
  }

  if (fileName) {
    if (fileName.endsWith(".pdf")) return "document";
    if (
      fileName.endsWith(".mp4") ||
      fileName.endsWith(".3gp") ||
      fileName.endsWith(".3gpp") ||
      fileName.endsWith(".webm") ||
      fileName.endsWith(".mov")
    ) {
      return "video";
    }
    if (
      fileName.endsWith(".mp3") ||
      fileName.endsWith(".m4a") ||
      fileName.endsWith(".aac") ||
      fileName.endsWith(".ogg") ||
      fileName.endsWith(".wav")
    ) {
      return "audio";
    }
  }

  return explicit;
};

const formatTimeShort = sentAt => {
  if (!sentAt) return null;
  try {
    return new Date(sentAt).toLocaleTimeString([], {
      hour: "2-digit",
      minute: "2-digit",
    });
  } catch {
    return null;
  }
};

const looksLikeAutoGeneratedMediaText = ({ text, mediaType, fileName }) => {
  const raw = String(text || "").trim();
  if (!raw) return false;

  const mt = String(mediaType || "")
    .trim()
    .toLowerCase();
  const name = String(fileName || "")
    .trim()
    .toLowerCase();

  const norm = raw
    .toLowerCase()
    .replace(/\s+/g, " ")
    .trim();

  if (name && norm === name) return true;

  if (mt === "image") {
    if (norm === "photo" || norm === "image") return true;
    if (norm.startsWith("image sent")) return true;
    if (norm.startsWith("photo sent")) return true;
    return false;
  }

  if (mt === "document") {
    if (norm === "pdf" || norm === "document") return true;
    if (norm.startsWith("pdf sent")) return true;
    if (norm.startsWith("document sent")) return true;
    return false;
  }

  if (mt === "video") {
    if (norm === "video") return true;
    if (norm.startsWith("video sent")) return true;
    return false;
  }

  if (mt === "audio") {
    if (norm === "audio") return true;
    if (norm.startsWith("audio sent")) return true;
    return false;
  }

  if (mt === "location") {
    if (norm === "location") return true;
    if (norm.startsWith("location sent")) return true;
    return false;
  }

  return false;
};

